package com.transformco.hs.ccn.offersearch.domain;

public class DispositonReasons {
	
	private String disposition;
	private String dispostionDescription;
	private String createUserId;
	private String createdTimestamp;
	private String updateUserId;
	private String updatedTimestamp;
	
	public String getDisposition() {
		return disposition;
	}
	public void setDisposition(String disposition) {
		this.disposition = disposition;
	}
	public String getDispostionDescription() {
		return dispostionDescription;
	}
	public void setDispostionDescription(String dispostionDescription) {
		this.dispostionDescription = dispostionDescription;
	}
	public String getCreateUserId() {
		return createUserId;
	}
	public void setCreateUserId(String createUserId) {
		this.createUserId = createUserId;
	}
	public String getCreatedTimestamp() {
		return createdTimestamp;
	}
	public void setCreatedTimestamp(String createdTimestamp) {
		this.createdTimestamp = createdTimestamp;
	}
	public String getUpdateUserId() {
		return updateUserId;
	}
	public void setUpdateUserId(String updateUserId) {
		this.updateUserId = updateUserId;
	}
	public String getUpdatedTimestamp() {
		return updatedTimestamp;
	}
	public void setUpdatedTimestamp(String updatedTimestamp) {
		this.updatedTimestamp = updatedTimestamp;
	}
	

}

package com.transformco.hs.ccn.offersearch.domain;

public class OfferPrograms {
	
private int offerProgramId;
	
	private String offerProgram;
	
	private String offerProgramDescription;
	
	private String startDate;
	
	private String endDate;
	
	public int getOfferProgramId() {
		return offerProgramId;
	}

	public void setOfferProgramId(int offerProgramId) {
		this.offerProgramId = offerProgramId;
	}

	public String getOfferProgram() {
		return offerProgram;
	}

	public void setOfferProgram(String offerProgram) {
		this.offerProgram = offerProgram;
	}

	public String getOfferProgramDescription() {
		return offerProgramDescription;
	}

	public void setOfferProgramDescription(String offerProgramDescription) {
		this.offerProgramDescription = offerProgramDescription;
	}

	public String getStartDate() {
		return startDate;
	}

	public void setStartDate(String startDate) {
		this.startDate = startDate;
	}

	public String getEndDate() {
		return endDate;
	}

	public void setEndDate(String endDate) {
		this.endDate = endDate;
	}

}
